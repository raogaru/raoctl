-- TITLE1: Rule Evaluation Context Tables
-- TITLE2: Displaying Information About the Tables Used by an Evaluation Context
-- DESC:

COLUMN TABLE_ALIAS HEADING 'Table Alias' FORMAT A20
COLUMN TABLE_NAME HEADING 'Table Name' FORMAT A40

SELECT TABLE_ALIAS,
       TABLE_NAME
  FROM DBA_EVALUATION_CONTEXT_TABLES
ORDER BY  EVALUATION_CONTEXT_OWNER, EVALUATION_CONTEXT_NAME;
